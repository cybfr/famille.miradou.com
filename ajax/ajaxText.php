	<h3>Jean Giono</h3>
	<h4>1953</h4>
	<p>
		<i>Voir au <a href="#Droits_d.27auteur"
			title="L’homme qui plantait des arbres">bas de la page</a> pour ce
			qui concerne le copyright.
