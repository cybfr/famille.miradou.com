	<h2>
		<span class="mw-headline">Droits d'auteur</span>
	</h2>
	<p>La nouvelle de Jean Giono ci-dessus a été écrite vers 1953. Traduite
		en treize langues, elle a été largement diffusée dans le monde entier
		et si appréciée que de nombreuses questions ont été posées sur la
		personnalité d'Elzéard Bouffier et sur la forêt de Vergons, ce qui a
		permis de retrouver le texte. Si l'homme qui plantait des chênes est
		le produit de l'imagination de l'auteur, il y a eu effectivement dans
		cette région un énorme effort de reboisement surtout depuis 1880. Cent
		mille hectares ont été reboisés avant la Première Guerre mondiale,
		surtout en pin noir d'Autriche et en mélèze d'Europe, ce sont
		aujourd'hui de belles forêts qui ont effectivement transformé le
		paysage et le régime des eaux.</p>
	<p>Voici d'ailleurs le texte de la lettre que Giono écrivit au
		Conservateur des Eaux et Forêts de Digne, Monsieur Valdeyron, en 1957,
		au sujet de cette nouvelle&nbsp;:</p>
	<p>
		<i>Cher Monsieur,</i>
	</p>
	<p>
		<i>Navré de vous décevoir, mais Elzéard Bouffier est un personnage
			inventé. Le but était de faire aimer l'arbre ou plus exactement faire
			aimer à planter des arbres (ce qui est depuis toujours une de mes
			idées les plus chères). Or si j'en juge par le résultat, le but a été
			atteint par ce personnage imaginaire. Le texte que vous avez lu dans
			Trees and Life a été traduit en Danois, Finlandais, Suédois,
			Norvégien, Anglais, Allemand, Russe, Tchécoslovaque, Hongrois,
			Espagnol, Italien, Yddisch, Polonais. J'ai donné mes droits
			gratuitement pour toutes les reproductions. Un américain est venu me
			voir dernièrement pour me demander l'autorisation de faire tirer ce
			texte à 100 000 exemplaires pour les répandre gratuitement en
			Amérique (ce que j'ai bien entendu accepté). L'Université de Zagreb
			en fait une traduction en yougoslave. C'est un de mes textes dont je
			suis le plus fier. Il ne me rapporte pas un centime et c'est pourquoi
			il accomplit ce pour quoi il a été écrit.</i>
	</p>
	<p>
		<i>J'aimerais vous rencontrer, s'il vous est possible, pour parler
			précisément de l'utilisation pratique de ce texte. Je crois qu'il est
			temps qu'on fasse une «&nbsp;politique de l'arbre&nbsp;» bien que le
			mot politique semble bien mal adapté.</i>
	</p>

	<p>
		<i>Très cordialement.</i>
	</p>

	<p>Jean Giono</p>
